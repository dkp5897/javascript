// x++, ++x, x--, --x

/* postfix: if operator used after operand i.e. x++ & x--

    return: the value before increment */
   
/* prefix: if operator used before operand i.e. ++x & --x

    increment before return the value */


    var a=4;
    console.log(a++);  //4

    a=8;
    console.log(++a);  //9

    a=10;
    console.log(a--);   //10

    a=20;
    console.log(--a);   //19
